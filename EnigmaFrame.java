    private void setRotor(String rotorSelection,int rotorNumber){
        if(rotorSelection.equals("I")){
            _eModel.changeRotor(rotorNumber,1);
        }else if(rotorSelection.equals("II")){
            _eModel.changeRotor(rotorNumber,2);
        }else if(rotorSelection.equals("III")){
            _eModel.changeRotor(rotorNumber,3);
        }else if(rotorSelection.equals("IV")){
            _eModel.changeRotor(rotorNumber,4);
        }else if(rotorSelection.equals("V")){
            _eModel.changeRotor(rotorNumber,5);
        }
    }

    private void refBoxActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String currentValue = (String)refBox.getSelectedItem();
        if(currentValue.equals("Reflector A")){
            _eModel.setReflector(1);
        }else if(currentValue.equals("Reflector B")){
            _eModel.setReflector(2);
        }else if(currentValue.equals("Reflector C")){
            _eModel.setReflector(3);
        }
    }


