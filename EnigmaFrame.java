    private void encodeButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String text = encodeBox.getText();
        text = text.toUpperCase();
        char[] c = text.toCharArray();
        resultBox.setText(Character.toString(_eModel.addLetter(c[0])));
        currentWordBox.setText(_eModel.getCurrentWord());
        encryptedWord.setText(_eModel.getCurrentEncryptedWord());
        encodeBox.setText("");
        int[] rotorSetting = _eModel.getRotorSetting();
        r3Setting.setText(Integer.toString(rotorSetting[2]));
        r2Setting.setText(Integer.toString(rotorSetting[1]));
        r1Setting.setText(Integer.toString(rotorSetting[0]));
    }

